func TestJq255eKeyGen(t *testing.T) {
	for i := 0; (2 * i) < len(kat_JQ255E_KEYGEN); i ++ {
		rsk, _ := hex.DecodeString(kat_JQ255E_KEYGEN[2 * i])
		rpk, _ := hex.DecodeString(kat_JQ255E_KEYGEN[2 * i + 1])
		sh := sha3.NewShake256()
		var bb [1]byte
		bb[0] = byte(i)
		sh.Write(bb[:])
		sk, _ := Jq255eGenerateKeyPair(sh)
		esk := sk.Encode(nil)
		epk := sk.Public().Encode(nil)
		if !bytes.Equal(esk, rsk) {
			t.Fatalf("Wrong private key generated:\nexp = %s\ngot = %s\n", hex.EncodeToString(rsk), hex.EncodeToString(esk))
		}
		if !bytes.Equal(epk, rpk) {
			t.Fatalf("Wrong public key generated:\nexp = %s\ngot = %s\n", hex.EncodeToString(rpk), hex.EncodeToString(epk))
		}
	}
}

func TestJq255eECDH(t *testing.T) {
	for i := 0; i < len(kat_JQ255E_ECDH); i += 5 {
		rsk, _ := hex.DecodeString(kat_JQ255E_ECDH[i])
		rppk1, _ := hex.DecodeString(kat_JQ255E_ECDH[i + 1])
		rsec1, _ := hex.DecodeString(kat_JQ255E_ECDH[i + 2])
		rppk2, _ := hex.DecodeString(kat_JQ255E_ECDH[i + 3])
		rsec2, _ := hex.DecodeString(kat_JQ255E_ECDH[i + 4])

		sk, _ := Jq255eDecodePrivateKey(rsk)

		sec, ok := Jq255eKeyExchange(sk, rppk1)
		if ok != 1 {
			t.Fatalf("Test %d: ECDH failed\n", i)
		}
		if !bytes.Equal(sec[:], rsec1) {
			t.Fatalf("Test %d: wrong secret:\nexp = %s\ngot = %s\n", i, hex.EncodeToString(rsec1), hex.EncodeToString(sec[:]))
		}

		sec, ok = Jq255eKeyExchange(sk, rppk2)
		if ok != 0 {
			t.Fatalf("Test %d: ECDH should have failed\n", i)
		}
		if !bytes.Equal(sec[:], rsec2) {
			t.Fatalf("Test %d: wrong secret:\nexp = %s\ngot = %s\n", i, hex.EncodeToString(rsec2), hex.EncodeToString(sec[:]))
		}
	}
}

func TestJq255eSign(t *testing.T) {
	for i := 0; i < len(kat_JQ255E_SIGN); i += 5 {
		rsk, _ := hex.DecodeString(kat_JQ255E_SIGN[i])
		rpk, _ := hex.DecodeString(kat_JQ255E_SIGN[i + 1])
		seed, _ := hex.DecodeString(kat_JQ255E_SIGN[i + 2])
		data, _ := hex.DecodeString(kat_JQ255E_SIGN[i + 3])
		rsig, _ := hex.DecodeString(kat_JQ255E_SIGN[i + 4])

		sk, _ := Jq255eDecodePrivateKey(rsk)
		pk := sk.Public()
		epub := pk.Encode(nil)
		if !bytes.Equal(epub, rpk) {
			t.Fatalf("Test %d: wrong public key:\nexp = %s\ngot = %s\n", i, hex.EncodeToString(rpk), hex.EncodeToString(epub))
		}
		sig, err := sk.signWithSeed(seed, data, crypto.Hash(crypto.BLAKE2s_256))
		if err != nil {
			t.Fatal(err)
		}
		if !bytes.Equal(sig, rsig) {
			t.Fatalf("Test %d: wrong signature:\nexp = %s\ngot = %s\n", i, hex.EncodeToString(rsig), hex.EncodeToString(sig))
		}
		if !pk.VerifyVartime(data, crypto.Hash(crypto.BLAKE2s_256), sig) {
			t.Fatalf("Test %d: signature was not verified\n", i)
		}
		data[0] ^= byte(0x01)
		if pk.VerifyVartime(data, crypto.Hash(crypto.BLAKE2s_256), sig) {
			t.Fatalf("Test %d: signature should not have been accepted\n", i)
		}
	}
}

func TestJq255eHashToCurve(t *testing.T) {
	var src [100]byte
	for i := 0; i < len(src); i ++ {
		src[i] = byte(i)
	}
	for i := 0; i < len(kat_JQ255E_HASHTOCURVE); i ++ {
		rp, _ := hex.DecodeString(kat_JQ255E_HASHTOCURVE[i])

		P, err := Jq255eHashToCurve(src[:i], crypto.Hash(0))
		if err != nil {
			t.Fatal(err)
		}
		ep := P.Encode(nil)
		if !bytes.Equal(ep, rp) {
			t.Fatalf("Test %d: wrong output:\nexp = %s\ngot = %s\n", i, hex.EncodeToString(rp), hex.EncodeToString(ep))
		}
	}
}

func TestJq255eHashToCurvePreHashed(t *testing.T) {
	var src [10]byte
	for i := 0; i < len(src); i ++ {
		src[i] = byte(i)
	}
	for i := 0; i < len(kat_JQ255E_HASHTOCURVE_PREHASHED); i ++ {
		rp, _ := hex.DecodeString(kat_JQ255E_HASHTOCURVE_PREHASHED[i])

		sh, _ := blake2s.New256(nil);
		sh.Write(src[i:i+1])
		var bb [32]byte
		sh.Sum(bb[:0])
		P, _ := Jq255eHashToCurve(bb[:], crypto.Hash(crypto.BLAKE2s_256))
		ep := P.Encode(nil)
		if !bytes.Equal(ep, rp) {
			t.Fatalf("Test %d: wrong output:\nexp = %s\ngot = %s\n", i, hex.EncodeToString(rp), hex.EncodeToString(ep))
		}
	}
}

